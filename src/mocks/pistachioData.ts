// --- Technical Chart Data (per variety, per period) ---

function generateTechData(basePrice: number, volatility: number, count: number, trend: number) {
  const data: { time: string; price: number; open: number; high: number; low: number; close: number }[] = [];
  let currentPrice = basePrice * 0.85;
  for (let i = 0; i < count; i++) {
    const noise = (Math.random() - 0.45) * volatility;
    const trendEffect = (i / count) * trend;
    const open = currentPrice;
    const closePrice = open + noise + trendEffect * volatility * 0.3;
    const high = Math.max(open, closePrice) + Math.random() * volatility * 0.4;
    const low = Math.min(open, closePrice) - Math.random() * volatility * 0.4;
    data.push({
      time: count <= 7
        ? `روز ${count - i}`
        : count <= 30
        ? `روز ${count - i}`
        : `روز ${count - i}`,
      price: Math.round(closePrice),
      open: Math.round(open),
      high: Math.round(high),
      low: Math.round(low),
      close: Math.round(closePrice),
    });
    currentPrice = closePrice;
  }
  return data.reverse();
}

